import { createHash, randomBytes } from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { groth16PackageRoot, groth16WorkspacePaths, resolveGroth16WorkspaceRoot } from "./paths.mjs";
import { captureSnarkjs, runSnarkjs } from "./snarkjs.mjs";

const CIRCUIT_NAME = "updateTree";
const CURVE = "bls12-381";
const BEACON_ITERATIONS = "10";

export async function generateLocalTrustedSetup({
  workspaceRoot = resolveGroth16WorkspaceRoot(),
  metadata,
  r1csPath,
} = {}) {
  const paths = groth16WorkspacePaths(workspaceRoot);
  const resolvedR1csPath = path.resolve(r1csPath ?? paths.r1csPath);
  assertFile("compiled updateTree r1cs", resolvedR1csPath);

  const normalizedMetadata = normalizeMetadata(metadata);
  const constraintCount = readConstraintCount(resolvedR1csPath, paths.rootDir, paths.rootDir);
  const power = nextPowerOfTwoExponent(constraintCount + 1);
  const workDir = path.join(paths.tmpDir, "trusted-setup");
  const ptau0 = path.join(workDir, "powersoftau_0000.ptau");
  const ptau1 = path.join(workDir, "powersoftau_0001.ptau");
  const ptauBeacon = path.join(workDir, "powersoftau_beacon.ptau");
  const phase1FinalPtau = path.join(workDir, `phase1_final_${String(power).padStart(2, "0")}.ptau`);
  const zkey0 = path.join(workDir, "circuit_0000.zkey");
  const zkey1 = path.join(workDir, "circuit_0001.zkey");

  fs.rmSync(workDir, { recursive: true, force: true });
  fs.rmSync(paths.crsDir, { recursive: true, force: true });
  fs.mkdirSync(workDir, { recursive: true });
  fs.mkdirSync(paths.crsDir, { recursive: true });

  const phase1ContributionEntropy = randomEntropy("phase1 contribution");
  const phase1Beacon = randomHex(32);
  const phase2ContributionEntropy = randomEntropy("phase2 contribution");
  const phase2Beacon = randomHex(32);

  runSnarkjs(["powersoftau", "new", CURVE, String(power), ptau0], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs([
    "powersoftau",
    "contribute",
    ptau0,
    ptau1,
    "--name=local updateTree phase1 contribution",
    `--entropy=${phase1ContributionEntropy}`,
  ], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs(["powersoftau", "beacon", ptau1, ptauBeacon, phase1Beacon, BEACON_ITERATIONS], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs(["powersoftau", "prepare", "phase2", ptauBeacon, phase1FinalPtau], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs(["groth16", "setup", resolvedR1csPath, phase1FinalPtau, zkey0], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs([
    "zkey",
    "contribute",
    zkey0,
    zkey1,
    "--name=local updateTree phase2 contribution",
    `--entropy=${phase2ContributionEntropy}`,
  ], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs([
    "zkey",
    "beacon",
    zkey1,
    paths.zkeyPath,
    phase2Beacon,
    BEACON_ITERATIONS,
    "--name=local updateTree phase2 beacon",
  ], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs(["zkey", "verify", resolvedR1csPath, phase1FinalPtau, paths.zkeyPath], paths.rootDir, { workspaceRoot: paths.rootDir });
  runSnarkjs(["zkey", "export", "verificationkey", paths.zkeyPath, paths.verificationKeyPath], paths.rootDir, { workspaceRoot: paths.rootDir });

  const generatedAt = new Date().toISOString();
  const outputMetadata = {
    circuit: CIRCUIT_NAME,
    tokamakL2JsVersion: normalizedMetadata.tokamakL2JsVersion,
    mtDepth: normalizedMetadata.mtDepth,
    constraintCount,
    powersOfTauPower: power,
    generatedAt,
    phase1Source: {
      ceremony: "local snarkjs powersoftau",
      source: "local-trusted-setup",
    },
    note: "Local trusted setup generated by tokamak-groth16 --install --trusted-setup. Security depends on local entropy and toxic-waste disposal.",
    phase2Contributions: 1,
    phase2BeaconApplied: true,
  };
  writeJson(paths.metadataPath, outputMetadata);

  const phase1FinalPtauSha256 = sha256FileSync(phase1FinalPtau);
  writeJson(paths.provenancePath, {
    generated_at_utc: generatedAt,
    backend_version: resolveGroth16PackageVersion(),
    source: "local-trusted-setup",
    circuit: CIRCUIT_NAME,
    constraint_count: constraintCount,
    powers_of_tau_power: power,
    phase1_source_provenance: {
      local_snarkjs_powersoftau: {
        curve: CURVE,
        power,
        beacon_sha256: sha256Text(phase1Beacon),
        beacon_iterations: Number(BEACON_ITERATIONS),
      },
    },
    phase1_final_ptau_file: path.basename(phase1FinalPtau),
    phase1_final_ptau_sha256: phase1FinalPtauSha256,
    phase2_source_provenance: {
      local_snarkjs_groth16: {
        contributions: 1,
        beacon_sha256: sha256Text(phase2Beacon),
        beacon_iterations: Number(BEACON_ITERATIONS),
      },
    },
    zkey_sha256: sha256FileSync(paths.zkeyPath),
    metadata_sha256: sha256FileSync(paths.metadataPath),
    verification_key_sha256: sha256FileSync(paths.verificationKeyPath),
    published_folder_url: null,
    published_archive_name: null,
    zkey_download_url: null,
  });

  fs.rmSync(workDir, { recursive: true, force: true });

  return {
    source: "local-trusted-setup",
    workspaceRoot: paths.rootDir,
    installedFiles: [
      "circuit_final.zkey",
      "verification_key.json",
      "metadata.json",
      "zkey_provenance.json",
    ],
    constraintCount,
    powersOfTauPower: power,
  };
}

function normalizeMetadata(metadata) {
  const mtDepth = Number(metadata?.mtDepth);
  if (!Number.isSafeInteger(mtDepth) || mtDepth <= 0) {
    throw new Error(`Invalid Groth16 circuit MT_DEPTH: ${metadata?.mtDepth}`);
  }
  const tokamakL2JsVersion = String(metadata?.tokamakL2JsVersion ?? "").trim();
  if (!tokamakL2JsVersion) {
    throw new Error("tokamakL2JsVersion must be a non-empty string.");
  }
  return { mtDepth, tokamakL2JsVersion };
}

function readConstraintCount(r1csPath, cwd, workspaceRoot) {
  const info = stripAnsi(captureSnarkjs(["r1cs", "info", r1csPath], cwd, { workspaceRoot }));
  const match = info.match(/# of Constraints:\s+(\d+)/);
  if (!match) {
    throw new Error("Failed to parse constraint count from snarkjs r1cs info output.");
  }
  return Number(match[1]);
}

function stripAnsi(value) {
  return value.replace(/\u001b\[[0-9;]*m/g, "");
}

function nextPowerOfTwoExponent(value) {
  let power = 0;
  let limit = 1;
  while (limit < value) {
    limit *= 2;
    power += 1;
  }
  return power;
}

function randomEntropy(label) {
  return `${label}: ${randomBytes(32).toString("hex")}`;
}

function randomHex(byteLength) {
  return randomBytes(byteLength).toString("hex");
}

function resolveGroth16PackageVersion() {
  const packageJson = JSON.parse(fs.readFileSync(path.join(groth16PackageRoot, "package.json"), "utf8"));
  if (typeof packageJson.version === "string" && packageJson.version.length > 0) {
    return packageJson.version;
  }
  throw new Error("packages/groth16/package.json is missing a package version.");
}

function writeJson(filePath, value) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function sha256FileSync(filePath) {
  return createHash("sha256").update(fs.readFileSync(filePath)).digest("hex");
}

function sha256Text(value) {
  return createHash("sha256").update(value).digest("hex");
}

function assertFile(label, filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing ${label}: ${filePath}`);
  }
}
