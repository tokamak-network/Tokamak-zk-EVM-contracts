#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { createRequire } from "node:module";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..", "..");
const templatePath = path.join(repoRoot, "groth16", "circuits", "src", "circuit_updateTree.template.circom");
const renderedCircuitPath = path.join(repoRoot, "groth16", "circuits", "src", "circuit_updateTree.circom");
const require = createRequire(import.meta.url);

function resolveTokamakPackageJsonPath() {
  const entryPath = require.resolve("tokamak-l2js");
  const packageRoot = entryPath.includes(`${path.sep}dist${path.sep}`)
    ? entryPath.slice(0, entryPath.lastIndexOf(`${path.sep}dist${path.sep}`))
    : path.dirname(entryPath);
  return path.join(packageRoot, "package.json");
}

function renderCircuit(mtDepth, version) {
  const template = fs.readFileSync(templatePath, "utf8");
  const body = template.split("__MT_DEPTH__").join(String(mtDepth));
  const rendered = [
    "pragma circom 2.2.2;",
    "",
    'include "./templates.circom";',
    "",
    "// Generated by scripts/groth16/render-update-tree-circuit.mjs.",
    "// Source template: groth16/circuits/src/circuit_updateTree.template.circom",
    `// tokamak-l2js version: ${version}`,
    `// MT_DEPTH: ${mtDepth}`,
    body.split("\n").slice(3).join("\n"),
  ].join("\n");

  fs.writeFileSync(renderedCircuitPath, rendered.endsWith("\n") ? rendered : `${rendered}\n`);
}

async function main() {
  const packageJsonPath = resolveTokamakPackageJsonPath();
  const packageJson = JSON.parse(fs.readFileSync(packageJsonPath, "utf8"));
  const tokamak = await import("tokamak-l2js");
  const mtDepth = Number(tokamak.MT_DEPTH);
  if (!Number.isInteger(mtDepth) || mtDepth <= 0) {
    throw new Error(`Invalid tokamak-l2js MT_DEPTH: ${String(tokamak.MT_DEPTH)}`);
  }

  renderCircuit(mtDepth, packageJson.version);
  console.log(
    `Rendered ${path.relative(process.cwd(), renderedCircuitPath)} from tokamak-l2js@${packageJson.version} MT_DEPTH=${mtDepth}.`,
  );
}

await main();
